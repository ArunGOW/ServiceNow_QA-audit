import { createContext, useContext, useState, useEffect } from "react";
import axios from "axios";

const AuthContext = createContext();

export const AuthProvider = ({ children }) => {
  const [user, setUser] = useState(null);

  // ðŸ”¹ Try to restore session from localStorage on reload
  useEffect(() => {
    const storedUser = localStorage.getItem("user");
    if (storedUser) {
      setUser(JSON.parse(storedUser));
    }
  }, []);

  const login = async (credentials) => {
    try {
      const res = await axios.post("http://localhost:8000/api/users/login-google", credentials);

      if (res.data.status === "success") {
        const userData = {
          user_sid: res.data.user_sid,
          user_type: res.data.user_type,
          token: res.data.session_token,   // âœ… map session_token â†’ token
        };

        setUser(userData);
        localStorage.setItem("user", JSON.stringify(userData));

        console.log("âœ… User logged in:", userData);
      } else {
        console.error("âŒ Login failed:", res.data);
      }
    } catch (error) {
      console.error("âŒ Login error:", error.response?.data || error.message);
    }
  };

  const logout = () => {
    setUser(null);
    localStorage.removeItem("user");
  };

  return (
    <AuthContext.Provider value={{ user, login, logout }}>
      {children}
    </AuthContext.Provider>
  );
};

export const useAuth = () => useContext(AuthContext);
